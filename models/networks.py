def color_image(image, normalized_temp, device):
    rgbs = []
    colors = []
    for temp in normalized_temp:
        if temp < 0.5:
            r = torch.tensor([1]).to(device)
            g = 1.142128472 * temp + 0.433689763
            b =1.913695637 * temp + 0.068589126
        else:
            r = -0.727725328 * temp + 1.271420566
            g = -0.533380367 * temp + 1.179172445
            b = torch.tensor([1]).to(device)
        color = torch.stack((r,g,b), dim=0)
        rgbs.append(color.reshape(-1, 1, 1))
        colors.append(color)
    rgbs = torch.stack(rgbs, 0)
    rgbs = rgbs.to(image.device).float()
    image = torch.cat([image] * 3, 1)
    image = image * rgbs
    return color, image


def reduction_batch_based(image_loss, M):
    # average of all valid pixels of the batch

    # avoid division by 0 (if sum(M) = sum(sum(mask)) = 0: sum(image_loss) = 0)
    divisor = torch.sum(M)

    if divisor == 0:
        return 0
    else:
        return torch.abs(torch.sum(image_loss) / divisor)


def gradient_loss(pred, grnd, mask):
    M = torch.sum(mask, (2, 3))

    diff = pred - grnd
    diff = torch.mul(mask, diff)
    grad_x = torch.abs(diff[:, :, :, 1:] - diff[:, :, :, :-1])
    mask_x = torch.mul(mask[:, :, :, 1:], mask[:, :, :, :-1])
    grad_x = torch.mul(mask_x, grad_x)
    grad_y = torch.abs(diff[:, :, 1:, :] - diff[:, :, :-1, :])
    mask_y = torch.mul(mask[:, :, 1:, :], mask[:, :, :-1, :])
    grad_y = torch.mul(mask_y, grad_y)
    image_loss = torch.sum(grad_x, (2, 3)) + torch.sum(grad_y, (2, 3))

    return reduction_batch_based(image_loss, M)


def l1_loss(pred, grnd, mask):
    l1 = torch.nn.functional.l1_loss(pred, grnd, reduction='none') * mask
    M = torch.sum(mask, (2, 3))
    return reduction_batch_based(l1, M)


def l1_grad_loss(pred, grnd, no_gradient_loss, mask=None):
    if mask == None:
        mask = torch.ones(pred.shape).to(pred.device)

    mae_term = l1_loss(pred, grnd, mask)

    grad_term = 0.0
    if not no_gradient_loss:
        for ds in range(4):
            scale = pow(2, ds)

            pred_scale = pred[:, ::scale, ::scale]
            grnd_scale = grnd[:, ::scale, ::scale]
            mask_scale = mask[:, ::scale, ::scale]

            grad_term += gradient_loss(pred_scale, grnd_scale, mask_scale)

    return mae_term + (0.5 * grad_term)


def get_norm_layer(norm_type='instance'):
    """Return a normalization layer

    Parameters:
        norm_type (str) -- the name of the normalization layer: batch | instance | none

    For BatchNorm, we use learnable affine parameters and track running statistics (mean/stddev).
    For InstanceNorm, we do not use learnable affine parameters. We do not track running statistics.
    """
    if norm_type == 'batch':
        norm_layer = functools.partial(nn.BatchNorm2d, affine=True, track_running_stats=True)
    elif norm_type == 'instance':
        norm_layer = functools.partial(nn.InstanceNorm2d, affine=False, track_running_stats=False)
    elif norm_type == 'none':
        def norm_layer(x): return Identity()
    else:
        raise NotImplementedError('normalization layer [%s] is not found' % norm_type)
    return norm_layer


def get_scheduler(optimizer, opt):
    """Return a learning rate scheduler

    Parameters:
        optimizer          -- the optimizer of the network
        opt (option class) -- stores all the experiment flags; needs to be a subclass of BaseOptions．　
                              opt.lr_policy is the name of learning rate policy: linear | step | plateau | cosine

    For 'linear', we keep the same learning rate for the first <opt.n_epochs> epochs
    and linearly decay the rate to zero over the next <opt.n_epochs_decay> epochs.
    For other schedulers (step, plateau, and cosine), we use the default PyTorch schedulers.
    See https://pytorch.org/docs/stable/optim.html for more details.
    """
    if opt.lr_policy == 'linear':
        def lambda_rule(epoch):
            lr_l = 1.0 - max(0, epoch + opt.epoch_count - opt.n_epochs) / float(opt.n_epochs_decay + 1)
            return lr_l
        scheduler = lr_scheduler.LambdaLR(optimizer, lr_lambda=lambda_rule)
    elif opt.lr_policy == 'step':
        scheduler = lr_scheduler.StepLR(optimizer, step_size=opt.lr_decay_iters, gamma=0.1)
    elif opt.lr_policy == 'plateau':
        scheduler = lr_scheduler.ReduceLROnPlateau(optimizer, mode='min', factor=0.2, threshold=0.01, patience=5)
    elif opt.lr_policy == 'cosine':
        scheduler = lr_scheduler.CosineAnnealingLR(optimizer, T_max=opt.n_epochs, eta_min=0)
    else:
        return NotImplementedError('learning rate policy [%s] is not implemented', opt.lr_policy)
    return scheduler


def init_weights(net, init_type='normal', init_gain=0.02):
    """Initialize network weights.

    Parameters:
        net (network)   -- network to be initialized
        init_type (str) -- the name of an initialization method: normal | xavier | kaiming | orthogonal
        init_gain (float)    -- scaling factor for normal, xavier and orthogonal.

    We use 'normal' in the original pix2pix and CycleGAN paper. But xavier and kaiming might
    work better for some applications. Feel free to try yourself.
    """
    def init_func(m):  # define the initialization function
        classname = m.__class__.__name__
        if hasattr(m, 'weight') and (classname.find('Conv') != -1 or classname.find('Linear') != -1):
            if init_type == 'normal':
                init.normal_(m.weight.data, 0.0, init_gain)
            elif init_type == 'xavier':
                init.xavier_normal_(m.weight.data, gain=init_gain)
            elif init_type == 'kaiming':
                init.kaiming_normal_(m.weight.data, a=0, mode='fan_in')
            elif init_type == 'orthogonal':
                init.orthogonal_(m.weight.data, gain=init_gain)
            else:
                raise NotImplementedError('initialization method [%s] is not implemented' % init_type)
            if hasattr(m, 'bias') and m.bias is not None:
                init.constant_(m.bias.data, 0.0)
        elif classname.find('BatchNorm2d') != -1:  # BatchNorm Layer's weight is not a matrix; only normal distribution applies.
            init.normal_(m.weight.data, 1.0, init_gain)
            init.constant_(m.bias.data, 0.0)

    print('initialize network with %s' % init_type)
    net.apply(init_func)  # apply the initialization function <init_func>


def init_net(net, init_type='normal', init_gain=0.02, gpu_ids=[]):
    """Initialize a network: 1. register CPU/GPU device (with multi-GPU support); 2. initialize the network weights
    Parameters:
        net (network)      -- the network to be initialized
        init_type (str)    -- the name of an initialization method: normal | xavier | kaiming | orthogonal
        gain (float)       -- scaling factor for normal, xavier and orthogonal.
        gpu_ids (int list) -- which GPUs the network runs on: e.g., 0,1,2

    Return an initialized network.
    """
    if len(gpu_ids) > 0:
        assert(torch.cuda.is_available())
        net.to(gpu_ids[0])
        net = torch.nn.DataParallel(net, gpu_ids)  # multi-GPUs
    init_weights(net, init_type, init_gain=init_gain)
    return net


