class ResnetGenerator(nn.Module):
    """Resnet-based generator that consists of Resnet blocks between a few downsampling/upsampling operations.

    We adapt Torch code and idea from Justin Johnson's neural style transfer project(https://github.com/jcjohnson/fast-neural-style)
    """

    def __init__(self, input_nc, output_nc, ngf=256, norm_layer=nn.BatchNorm2d, use_dropout=False, n_blocks=6, padding_type='reflect', demodule=False, anti_alias=False, block="basic", groups=1, base_width=64):
        """Construct a Resnet-based generator

        Parameters:
            input_nc (int)      -- the number of channels in input images
            output_nc (int)     -- the number of channels in output images
            ngf (int)           -- the number of filters in the last conv layer
            norm_layer          -- normalization layer
            use_dropout (bool)  -- if use dropout layers
            n_blocks (int)      -- the number of ResNet blocks
            padding_type (str)  -- the name of padding layer in conv layers: reflect | replicate | zero
        """
        assert(n_blocks >= 0)
        super(ResnetGenerator, self).__init__()
        if type(norm_layer) == functools.partial:
            use_bias = norm_layer.func == nn.InstanceNorm2d
        else:
            use_bias = norm_layer == nn.InstanceNorm2d
        if demodule:
            model = [nn.ReflectionPad2d(3),
                     ModulatedConv2d(input_nc, ngf, kernel_size=7),norm_layer(ngf),
                     nn.ReLU(True)]
        else:
            model = [nn.ReflectionPad2d(3),
                     nn.Conv2d(input_nc, ngf, kernel_size=7, padding=0, bias=use_bias),
                     norm_layer(ngf),
                     nn.ReLU(True)]

        n_downsampling = 2
        for i in range(n_downsampling):  # add downsampling layers
            mult = 2 ** i
            if demodule:
                model += [ModulatedConv2d(ngf * mult, ngf * mult * 2, kernel_size=3, downsample=True),norm_layer(ngf * mult * 2),
                          nn.ReLU(True)]
            elif anti_alias and i == 0:
                model += [nn.Conv2d(ngf * mult, ngf * mult * 2, kernel_size=3, stride=1, padding=1),
                          norm_layer(ngf * mult * 2),
                               nn.ReLU(inplace=True),
                          antialiased_cnns.BlurPool(ngf * mult * 2, stride=2),
                          ]
            else:
                model += [nn.Conv2d(ngf * mult, ngf * mult * 2, kernel_size=3, stride=2, padding=1, bias=use_bias),
                      norm_layer(ngf * mult * 2),
                      nn.ReLU(True)]
        # print(ngf * mult * 2)
        mult = 2 ** n_downsampling
        for i in range(n_blocks):       # add ResNet blocks
            if block == "basic":
                model += [ResnetBlock(ngf * mult, padding_type=padding_type, norm_layer=norm_layer, use_dropout=use_dropout, use_bias=use_bias, demodule=demodule)]
            else:
                model += [
                    ResnextBlock(ngf * mult, padding_type=padding_type, norm_layer=norm_layer, use_dropout=use_dropout,
                                use_bias=use_bias, demodule=demodule, groups=groups, base_width= base_width)]
        for i in range(n_downsampling):  # add upsampling layers
            mult = 2 ** (n_downsampling - i)
            # model += [nn.Upsample(scale_factor=2, mode='nearest'),
            # nn.ReflectionPad2d(1),
            # nn.Conv2d(ngf * mult, int(ngf * mult / 2),
            #           kernel_size=3, stride=1, padding=0),norm_layer(int(ngf * mult / 2)),
            #           nn.ReLU(True)]
            if demodule:
                model += [ModulatedConv2d(ngf * mult, int(ngf * mult / 2), kernel_size=3,upsample=True),
                          norm_layer(int(ngf * mult / 2)),
                          nn.ReLU(True)]
            elif anti_alias and i == 0:
                model += [nn.ConvTranspose2d(ngf * mult, int(ngf * mult / 2), kernel_size=3, stride=2, padding=1, output_padding=1, bias=use_bias),
                            norm_layer(int(ngf * mult / 2)),
                               nn.ReLU(inplace=True),
                          antialiased_cnns.BlurPool(int(ngf * mult / 2), stride=1)]
            else:
                model += [nn.ConvTranspose2d(ngf * mult, int(ngf * mult / 2),
                                             kernel_size=3, stride=2,
                                             padding=1, output_padding=1,
                                             bias=use_bias),
                          norm_layer(int(ngf * mult / 2)),
                          nn.ReLU(True)]
        model += [nn.ReflectionPad2d(3)]
        if demodule:
            model += [ModulatedConv2d(ngf, output_nc, kernel_size=7)]
        else:
            model += [nn.Conv2d(ngf, output_nc, kernel_size=7, padding=0)]
        model += [nn.Tanh()]

        self.model = nn.Sequential(*model)

    def forward(self, input, layers=[], encode_only=False):
        # print(self.model)
        if -1 in layers:
            layers.append(len(self.model))
        if len(layers) > 0:
            feat = input
            feats = []
            for layer_id, layer in enumerate(self.model):
                # print(layer_id, layer)
                feat = layer(feat)
                if layer_id in layers:
                    # print("%d: adding the output of %s %d" % (layer_id, layer.__class__.__name__, feat.size(1)))
                    feats.append(feat)
                else:
                    # print("%d: skipping %s %d" % (layer_id, layer.__class__.__name__, feat.size(1)))
                    pass
                if layer_id == layers[-1] and encode_only:
                    # print('encoder only return features')
                    return feats  # return intermediate features alone; stop in the last layers

            return feat, feats  # return both output and intermediate features
        else:
            """Standard forward"""
            fake = self.model(input)
            return fake


